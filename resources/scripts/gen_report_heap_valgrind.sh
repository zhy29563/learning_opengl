#!/bin/bash

########################################################################################################################
# 目录变量
########################################################################################################################
SCT_DIR=$(cd $(dirname $0) && pwd) # scripts
RES_DIR=$(dirname ${SCT_DIR})      # reources
PJT_DIR=$(dirname ${RES_DIR})      # repo
BLT_DIR=${PJT_DIR}/build_heap_valgrind  # build

########################################################################################################################
# 依赖库编译环境变量
########################################################################################################################
ENVS_ROOT=${HOME}/.envs
LIBS_PATH=x86_64-linux-gnu-v9.4.0
LIBS_ROOT=${ENVS_ROOT}/libs/${LIBS_PATH}

########################################################################################################################
# 编译
########################################################################################################################
rm -rf "${BLT_DIR}"
mkdir -p "${BLT_DIR}" && cd "${BLT_DIR}"
cmake .. -G"Unix Makefiles" -DENABLE_TEST_HEAP_VALGRIND=ON
make -j16
########################################################################################################################
# 运行测试程序并生成报告
########################################################################################################################
valgrind --tool=memcheck  --leak-check=yes ${BLT_DIR}/apps/app_add/app_add
echo "ret = "$?